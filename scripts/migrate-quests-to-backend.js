// Воспроизводим данные из registry.ts / data файлов в чистом JS

function serializeAnswer(answerFn) {
    // Анализируем функцию ответа и конвертируем в JSON-конфиг для бэкенда
    const fnStr = answerFn.toString();

    // () => true — любой ответ
    if (fnStr.includes('=> true') || fnStr.includes('() => true')) {
        return { type: 'any', value: '' };
    }

    // s === 'xxx' or normalize(s) === 'xxx' — точное совпадение
    const exactMatch = fnStr.match(/(?:normalize\(s\)|s)\s*===\s*'([^']+)'/);
    if (exactMatch) {
        return { type: 'exact', value: exactMatch[1] };
    }

    // Inline normalize chain: s.toLowerCase()...trim() === 'xxx'
    const inlineNormExact = fnStr.match(/\.trim\(\)\s*===\s*'([^']+)'/);
    if (inlineNormExact) {
        return { type: 'exact', value: inlineNormExact[1] };
    }

    // ['a','b','c'].includes(normalize(s)) or ['a','b'].includes(s) — один из вариантов
    const includesMatch = fnStr.match(/\[([^\]]+)\]\.includes\((?:normalize\(s\)|s)\)/);
    if (includesMatch) {
        const values = includesMatch[1].replace(/'/g, '').split(',').map(v => v.trim());
        return { type: 'exact_any', value: JSON.stringify(values) };
    }

    // parseInt(s,10) === N — точное число
    const intExact = fnStr.match(/parseInt\(s,\s*10\)\s*===\s*(\d+)/);
    if (intExact) {
        return { type: 'exact', value: intExact[1] };
    }

    // n >= X && n <= Y — диапазон
    const rangeMatch = fnStr.match(/n\s*>=\s*(\d+)\s*&&\s*n\s*<=\s*(\d+)/);
    if (rangeMatch) {
        return { type: 'range', value: JSON.stringify({ min: parseInt(rangeMatch[1]), max: parseInt(rangeMatch[2]) }) };
    }

    // normalize(s).length > N or s.length > N — любой текст длиннее N
    const lenMatch = fnStr.match(/(?:normalize\(s\)|s)\.length\s*>\s*(\d+)/);
    if (lenMatch) {
        return { type: 'any_text', value: JSON.stringify({ min_length: parseInt(lenMatch[1]) + 1 }) };
    }

    // typeof s === 'string' && s.trim().length>0 — любой непустой
    if (fnStr.includes('s.trim().length>0') || fnStr.includes('s.trim().length > 0')) {
        return { type: 'any', value: '' };
    }

    // Math.abs(parseFloat(...) - X) < Y — приблизительное число
    const approxMatch = fnStr.match(/Math\.abs\(parseFloat.*?-\s*([\d.]+)\)\s*<\s*([\d.]+)/);
    if (approxMatch) {
        return { type: 'approx', value: JSON.stringify({ target: parseFloat(approxMatch[1]), tolerance: parseFloat(approxMatch[2]) }) };
    }

    // !Number.isNaN(parseInt(s,10)) — любое число
    if (fnStr.includes('!Number.isNaN(parseInt')) {
        return { type: 'any_number', value: '' };
    }

    // Сложные функции с несколькими вариантами (minsk dragon step 6, 7)
    const multiExact = [];
    const allExacts = fnStr.matchAll(/n\s*===\s*'([^']+)'/g);
    for (const m of allExacts) {
        multiExact.push(m[1]);
    }
    if (multiExact.length > 0) {
        return { type: 'exact_any', value: JSON.stringify(multiExact) };
    }

    // Fallback: сохраняем строку функции для ручной обработки
    console.warn('⚠️  Не удалось автоматически сериализовать answer:', fnStr.substring(0, 100));
    return { type: 'function', value: fnStr };
}

function serializeStep(step) {
    const answer = serializeAnswer(step.answer);
    return {
        id: step.id,
        title: step.title,
        location: step.location,
        story: step.story,
        task: step.task,
        hint: step.hint || null,
        answer_type: answer.type,
        answer_value: answer.value,
        lat: step.lat,
        lng: step.lng,
        maps_url: step.mapsUrl || '',
        image_url: null, // Изображения загружаются отдельно
        input_type: step.inputType || 'text',
    };
}

