import React, { memo, useEffect, useMemo, useState } from "react";
import {
    ScrollView,
    StyleSheet,
    Text,
    useWindowDimensions,
    View,
    Platform,
    InteractionManager,
} from "react-native";
import { Image as ExpoImage } from "expo-image";
import StableContent from "@/components/travel/StableContent";

interface TravelDescriptionProps {
    htmlContent: string;
    title?: string;          // ‚Üê –∑–∞–≥–æ–ª–æ–≤–æ–∫ –º–æ–∂–µ—Ç –æ—Ç—Å—É—Ç—Å—Ç–≤–æ–≤–∞—Ç—å
    noBox?: boolean;
}

/**
 * –û–ø–∏—Å–∞–Ω–∏–µ –ø—É—Ç–µ—à–µ—Å—Ç–≤–∏—è —Å –∞–¥–∞–ø—Ç–∏–≤–Ω–æ–π –≤—ã—Å–æ—Ç–æ–π –∏ –±–µ–∑–æ–ø–∞—Å–Ω–æ–π —Ä–∞–∑–º–µ—Ç–∫–æ–π.
 * - –ö–æ—Ä—Ä–µ–∫—Ç–Ω–æ —Ä–∞–±–æ—Ç–∞–µ—Ç –±–µ–∑ title.
 * - contentWidth –Ω–∏–∫–æ–≥–¥–∞ –Ω–µ —É—Ö–æ–¥–∏—Ç –≤ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è.
 * - –ü–ª–∞—à–∫–∞-—à—Ç–∞–º–ø –Ω–µ –ø–µ—Ä–µ—Ö–≤–∞—Ç—ã–≤–∞–µ—Ç –∫–ª–∏–∫–∏ –∏ –Ω–µ –ª–æ–º–∞–µ—Ç —Å–∫—Ä–æ–ª–ª.
 * - –ü–∞—Ä—Å–∏–Ω–≥ HTML –æ—Ç–∫–ª–∞–¥—ã–≤–∞–µ—Ç—Å—è –¥–æ idle, —á—Ç–æ–±—ã –Ω–µ –∂–µ—á—å main thread.
 */
const TravelDescription: React.FC<TravelDescriptionProps> = ({
                                                                 htmlContent,
                                                                 title,
                                                                 noBox = false,
                                                             }) => {
    const { width, height } = useWindowDimensions();

    // ---- —Ä–∞–∑–º–µ—Ä—ã –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞ ----
    const pageHeight = useMemo(() => Math.round(height * 0.7), [height]);
    const contentWidth = useMemo(() => {
        const maxContent = Math.min(width, 900);
        return Math.max(maxContent - 60, 220);
    }, [width]);

    // ---- —Å–æ—Å—Ç–æ—è–Ω–∏–µ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ ----
    const isEmptyHtml = useMemo(() => {
        if (!htmlContent) return true;
        const txt = String(htmlContent).trim().replace(/<[^>]+>/g, "");
        return txt.length === 0;
    }, [htmlContent]);

    // ---- –æ—Ç–ª–æ–∂–µ–Ω–Ω—ã–π —Ä–µ–Ω–¥–µ—Ä —Ç—è–∂—ë–ª–æ–≥–æ HTML ----
    const [canParseHtml, setCanParseHtml] = useState(Platform.OS !== "web" ? false : false);

    useEffect(() => {
        let cancelled = false;

        if (Platform.OS === "web") {
            const arm = () => !cancelled && setCanParseHtml(true);
            if ("requestIdleCallback" in window) {
                // @ts-ignore
                const id = (window as any).requestIdleCallback(arm, { timeout: 1200 });
                return () => {
                    cancelled = true;
                    // @ts-ignore
                    if ((window as any).cancelIdleCallback) (window as any).cancelIdleCallback(id);
                };
            } else {
                const t = setTimeout(arm, 800);
                return () => {
                    cancelled = true;
                    clearTimeout(t);
                };
            }
        } else {
            const task = InteractionManager.runAfterInteractions(() => {
                if (!cancelled) setCanParseHtml(true);
            });
            return () => {
                cancelled = true;
                task.cancel();
            };
        }
    }, [htmlContent]);

    const inner = (
        <View style={styles.inner} pointerEvents="box-none">
            {/* –ü–æ–ª—É–ø—Ä–æ–∑—Ä–∞—á–Ω—ã–π —à—Ç–∞–º–ø –≤ —É–≥–ª—É ‚Äî –∑–∞–≥—Ä—É–∂–∞–µ–º —Å –Ω–∏–∑–∫–∏–º –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–æ–º */}
            <ExpoImage
                source={require("@/assets/travel-stamp.webp")}
                style={styles.stamp}
                accessibilityIgnoresInvertColors
                accessible={false}
                pointerEvents="none"
                cachePolicy="memory-disk"
                priority="low"
            />

            {/* –ö–æ–Ω—Ç–µ–Ω—Ç */}
            {isEmptyHtml ? (
                <Text style={styles.placeholder}>–û–ø–∏—Å–∞–Ω–∏–µ —Å–∫–æ—Ä–æ –ø–æ—è–≤–∏—Ç—Å—è üôÇ</Text>
            ) : canParseHtml ? (
                <StableContent html={htmlContent} contentWidth={contentWidth} />
            ) : (
                // –ª—ë–≥–∫–∏–π –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä –Ω–∞ –≤—Ä–µ–º—è idle ‚Äî –¥–µ—à–µ–≤–ª–µ, —á–µ–º —Å—Ä–∞–∑—É –ø–∞—Ä—Å–∏—Ç—å HTML
                <Text style={styles.placeholder}>–ó–∞–≥—Ä—É–∂–∞–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ‚Ä¶</Text>
            )}
        </View>
    );

    return (
        <View style={styles.wrapper} testID="travel-description">
            {noBox ? (
                <ScrollView
                    style={styles.scrollArea}
                    contentContainerStyle={styles.scrollContent}
                    scrollEventThrottle={16}
                    showsVerticalScrollIndicator
                    keyboardShouldPersistTaps="handled"
                    // –Ω–µ–º–Ω–æ–≥–æ —ç–∫–æ–Ω–æ–º–∏–º –Ω–∞ web
                    {...(Platform.OS === "web" ? { overScrollMode: "never" as any } : {})}
                >
                    {inner}
                </ScrollView>
            ) : (
                <View style={[styles.fixedHeightBlock, { height: pageHeight }]}>
                    <ScrollView
                        style={styles.scrollArea}
                        contentContainerStyle={styles.scrollContent}
                        scrollEventThrottle={16}
                        showsVerticalScrollIndicator
                        keyboardShouldPersistTaps="handled"
                        {...(Platform.OS === "web" ? { overScrollMode: "never" as any } : {})}
                    >
                        {inner}
                    </ScrollView>
                </View>
            )}
        </View>
    );
};

export default memo(TravelDescription);

const styles = StyleSheet.create({
    wrapper: {
        alignSelf: "center",
        width: "100%",
        maxWidth: 900,
        paddingHorizontal: 20,
        paddingTop: 20,
        paddingBottom: 40,
        backgroundColor: "transparent",
    },

    inner: {
        position: "relative",
        paddingTop: 6,
    },

    placeholder: {
        textAlign: "center",
        color: "#6b7280",
        fontSize: 15,
        paddingVertical: 8,
    },

    fixedHeightBlock: {
        borderWidth: 1,
        borderColor: "#DDD",
        borderRadius: 10,
        backgroundColor: "#FFF",
        overflow: "hidden",
        shadowColor: "#000",
        shadowOpacity: 0.05,
        shadowRadius: 5,
        shadowOffset: { width: 0, height: 2 },
        elevation: 2,
    },

    scrollArea: {},

    scrollContent: {
        paddingBottom: 8,
    },

    stamp: {
        position: "absolute",
        top: 4,
        right: 4,
        width: 60,
        height: 60,
        opacity: 0.25,
        zIndex: 1,
    },
});
